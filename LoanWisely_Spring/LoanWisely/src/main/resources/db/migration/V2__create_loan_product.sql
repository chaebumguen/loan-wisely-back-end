-- 6) PRODUCT_INTEREST_RATE
CREATE TABLE PRODUCT_INTEREST_RATE (
  rate_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id  NUMBER NOT NULL,
  rate_base   NUMBER,
  rate_min    NUMBER,
  rate_max    NUMBER,
  as_of_date  DATE NOT NULL,
  CONSTRAINT CK_PIR_RATE_RANGE CHECK (
    (rate_min IS NULL OR rate_max IS NULL OR rate_min <= rate_max)
    AND (rate_base IS NULL OR rate_min IS NULL OR rate_base >= rate_min)
    AND (rate_base IS NULL OR rate_max IS NULL OR rate_base <= rate_max)
  )
);

ALTER TABLE PRODUCT_INTEREST_RATE
  ADD CONSTRAINT FK_PRODUCT_INTEREST_RATE_PRODUCT
  FOREIGN KEY (product_id) REFERENCES PRODUCT(product_id);

-- FK 인덱스 + (중복 방지) 유니크 권장
CREATE INDEX IX_PIR_PRODUCT ON PRODUCT_INTEREST_RATE(product_id);
CREATE UNIQUE INDEX UX_PIR_PRODUCT_ASOF ON PRODUCT_INTEREST_RATE(product_id, as_of_date);


-- 7) PRODUCT_RATE_SNAPSHOT
CREATE TABLE PRODUCT_RATE_SNAPSHOT (
  rate_id     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id  NUMBER NOT NULL,
  rate_min    NUMBER,
  rate_max    NUMBER,
  score_base  NUMBER,
  as_of_date  DATE NOT NULL,
  rate_type   VARCHAR2(100),
  CONSTRAINT CK_PRS_RATE_RANGE CHECK (
    rate_min IS NULL OR rate_max IS NULL OR rate_min <= rate_max
  )
);

ALTER TABLE PRODUCT_RATE_SNAPSHOT
  ADD CONSTRAINT FK_PRODUCT_RATE_SNAPSHOT_PRODUCT
  FOREIGN KEY (product_id) REFERENCES PRODUCT(product_id);

-- FK 인덱스 + (스냅샷 중복 방지) 유니크 권장
CREATE INDEX IX_PRS_PRODUCT ON PRODUCT_RATE_SNAPSHOT(product_id);
CREATE UNIQUE INDEX UX_PRS_PRODUCT_ASOF_TYPE ON PRODUCT_RATE_SNAPSHOT(product_id, as_of_date, rate_type);


-- 8) PRODUCT_ELIGIBILITY
CREATE TABLE PRODUCT_ELIGIBILITY (
  product_id        NUMBER PRIMARY KEY,
  min_age           NUMBER,
  max_age           NUMBER,
  min_income        NUMBER,
  max_dti           NUMBER,
  min_credit_score  NUMBER,
  max_dsr           NUMBER,
  employment_type   VARCHAR2(100),
  CONSTRAINT CK_PE_AGE CHECK (min_age IS NULL OR max_age IS NULL OR min_age <= max_age),
  CONSTRAINT CK_PE_NUM CHECK (
    (min_income IS NULL OR min_income >= 0)
    AND (max_dti IS NULL OR max_dti >= 0)
    AND (max_dsr IS NULL OR max_dsr >= 0)
    AND (min_credit_score IS NULL OR min_credit_score >= 0)
  )
);

ALTER TABLE PRODUCT_ELIGIBILITY
  ADD CONSTRAINT FK_PRODUCT_ELIGIBILITY_PRODUCT
  FOREIGN KEY (product_id) REFERENCES PRODUCT(product_id);


