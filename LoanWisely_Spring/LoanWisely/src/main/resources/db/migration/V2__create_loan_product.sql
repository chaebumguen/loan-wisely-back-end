-- 10) CODE_DICTIONARY
CREATE TABLE CODE_DICTIONARY (
  dict_id                           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  version_id                        NUMBER NOT NULL,
  column_code                       VARCHAR2(200) NOT NULL,
  column_name                       VARCHAR2(300),
  column_desc                       VARCHAR2(2000),
  large_category_category_value_id  NUMBER,
  mideum_category_category_value_id NUMBER, -- ※ 오탈자 가능( medium )이지만, 기존 사용중이면 변경 금지
