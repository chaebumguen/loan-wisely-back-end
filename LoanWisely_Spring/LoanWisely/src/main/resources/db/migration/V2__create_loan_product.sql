  ADD CONSTRAINT FK_CODE_DICT_VERSION
  FOREIGN KEY (version_id) REFERENCES CODE_DICTIONARY_VERSION(version_id);

ALTER TABLE CODE_DICTIONARY
  ADD CONSTRAINT FK_CODE_DICT_LARGE_CAT
  FOREIGN KEY (large_category_category_value_id) REFERENCES CATEGORY_VALUE(category_value_id);

ALTER TABLE CODE_DICTIONARY
  ADD CONSTRAINT FK_CODE_DICT_MID_CAT
  FOREIGN KEY (mideum_category_category_value_id) REFERENCES CATEGORY_VALUE(category_value_id);

ALTER TABLE CODE_DICTIONARY
  ADD CONSTRAINT FK_CODE_DICT_SMALL_CAT
  FOREIGN KEY (small_category_category_value_id) REFERENCES CATEGORY_VALUE(category_value_id);

-- FK 인덱스 권장(카테고리로 검색/조인 시)
