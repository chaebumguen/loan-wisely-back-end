CREATE INDEX IX_CODE_DICT_MID_CAT   ON CODE_DICTIONARY(mideum_category_category_value_id);
CREATE INDEX IX_CODE_DICT_SMALL_CAT ON CODE_DICTIONARY(small_category_category_value_id);


-- 11) CODE_DICTIONARY_DIFF
CREATE TABLE CODE_DICTIONARY_DIFF (
  diff_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  pre_version_id   NUMBER NOT NULL,
  post_version_id  NUMBER NOT NULL,
  change_type      VARCHAR2(20) NOT NULL, -- ADD/MOD/DEL
  column_code      VARCHAR2(200) NOT NULL,
  before_json_path VARCHAR2(1000),
  after_json_path  VARCHAR2(1000),
  created_at       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_CODE_DICT_DIFF_TYPE CHECK (change_type IN ('ADD','MOD','DEL')),
  CONSTRAINT CK_CODE_DICT_DIFF_VER  CHECK (pre_version_id <> post_version_id)
);

ALTER TABLE CODE_DICTIONARY_DIFF
  ADD CONSTRAINT FK_CODE_DICT_DIFF_PRE
  FOREIGN KEY (pre_version_id) REFERENCES CODE_DICTIONARY_VERSION(version_id);

ALTER TABLE CODE_DICTIONARY_DIFF
  ADD CONSTRAINT FK_CODE_DICT_DIFF_POST
  FOREIGN KEY (post_version_id) REFERENCES CODE_DICTIONARY_VERSION(version_id);

-- FK 인덱스 권장
CREATE INDEX IX_CODE_DICT_DIFF_PRE  ON CODE_DICTIONARY_DIFF(pre_version_id);
CREATE INDEX IX_CODE_DICT_DIFF_POST ON CODE_DICTIONARY_DIFF(post_version_id);


