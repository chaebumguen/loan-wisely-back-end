-- 37) RECO_ITEM
CREATE TABLE RECO_ITEM (
  item_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  result_id        NUMBER NOT NULL,
  product_id       NUMBER NOT NULL,
  matching_score   NUMBER,
  estimated_rate   NUMBER,
  estimated_limit  NUMBER,
  reason_json_path VARCHAR2(1000),
  rank             NUMBER,
  created_at       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_RECO_ITEM_NUM CHECK (
    (matching_score IS NULL OR matching_score >= 0)
    AND (estimated_rate IS NULL OR estimated_rate >= 0)
    AND (estimated_limit IS NULL OR estimated_limit >= 0)
    AND (rank IS NULL OR rank >= 0)
  )
);

CREATE INDEX IX_RECO_ITEM_RESULT ON RECO_ITEM(result_id);

ALTER TABLE RECO_ITEM
  ADD CONSTRAINT FK_RECO_ITEM_RESULT
  FOREIGN KEY (result_id) REFERENCES RECO_RESULT(result_id);

ALTER TABLE RECO_ITEM
  ADD CONSTRAINT FK_RECO_ITEM_PRODUCT
  FOREIGN KEY (product_id) REFERENCES PRODUCT(product_id);

-- FK 인덱스 권장
CREATE INDEX IX_RECO_ITEM_PRODUCT ON RECO_ITEM(product_id);

-- (선택) 결과 내 동일 상품 중복 방지 + 랭크 중복 방지
CREATE UNIQUE INDEX UX_RECO_ITEM_RESULT_PRODUCT ON RECO_ITEM(result_id, product_id);
CREATE UNIQUE INDEX UX_RECO_ITEM_RESULT_RANK    ON RECO_ITEM(result_id, rank);


