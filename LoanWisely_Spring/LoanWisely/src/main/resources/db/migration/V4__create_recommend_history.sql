-- 34) RECO_POLICY
CREATE TABLE RECO_POLICY (
  policy_id                     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  version                       VARCHAR2(100) NOT NULL,
  policy_type_category_value_id NUMBER NOT NULL,
  policy_key                    VARCHAR2(200) NOT NULL,
  policy_value                  VARCHAR2(2000),
  effective_from                TIMESTAMP,
  effective_to                  TIMESTAMP,
  created_at                    TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_RECO_POLICY_EFFECTIVE CHECK (
    effective_from IS NULL OR effective_to IS NULL OR effective_from <= effective_to
  )
);

ALTER TABLE RECO_POLICY
  ADD CONSTRAINT FK_RECO_POLICY_TYPE
  FOREIGN KEY (policy_type_category_value_id) REFERENCES CATEGORY_VALUE(category_value_id);

CREATE INDEX IX_RECO_POLICY_TYPE ON RECO_POLICY(policy_type_category_value_id);

-- (선택) 동일 버전/타입/키 중복 방지
CREATE UNIQUE INDEX UX_RECO_POLICY_VER_TYPE_KEY
  ON RECO_POLICY(version, policy_type_category_value_id, policy_key);


