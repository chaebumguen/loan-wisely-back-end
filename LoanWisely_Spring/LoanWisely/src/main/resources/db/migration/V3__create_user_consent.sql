-- 19) USER_CONSENT
CREATE TABLE USER_CONSENT (
  consent_id                     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id                        NUMBER NOT NULL,
  consent_type_category_value_id NUMBER NOT NULL,
  agreed_yn                      CHAR(1) DEFAULT 'N' NOT NULL,
  agreed_at                      TIMESTAMP,
  expired_at                     TIMESTAMP,
  CONSTRAINT CK_USER_CONSENT_AGREED CHECK (agreed_yn IN ('Y','N')),
  CONSTRAINT CK_USER_CONSENT_TIME CHECK (agreed_at IS NULL OR expired_at IS NULL OR agreed_at <= expired_at)
);

ALTER TABLE USER_CONSENT
  ADD CONSTRAINT FK_USER_CONSENT_USER
  FOREIGN KEY (user_id) REFERENCES USER_PROFILE(user_id);

ALTER TABLE USER_CONSENT
  ADD CONSTRAINT FK_USER_CONSENT_TYPE
  FOREIGN KEY (consent_type_category_value_id) REFERENCES CATEGORY_VALUE(category_value_id);

-- FK 인덱스 권장
CREATE INDEX IX_USER_CONSENT_USER ON USER_CONSENT(user_id);
CREATE INDEX IX_USER_CONSENT_TYPE ON USER_CONSENT(consent_type_category_value_id);

