-- 27) USER_RISK_DETAIL
CREATE TABLE USER_RISK_DETAIL (
  risk_id          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id          NUMBER NOT NULL,
  credit_score     NUMBER,
  dti              NUMBER,
  dsr              NUMBER,
  repayment_cap    NUMBER,
  stability_score  NUMBER,
  normalized_soc   VARCHAR2(1000), -- JSON "본문"이 아니라 "경로"라면 path 컬럼명으로 바꾸는 게 더 명확합니다.
  created_at       TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT CK_USER_RISK_DETAIL_NUM CHECK (
    (credit_score IS NULL OR credit_score >= 0)
    AND (dti IS NULL OR dti >= 0)
    AND (dsr IS NULL OR dsr >= 0)
    AND (repayment_cap IS NULL OR repayment_cap >= 0)
  )
);

CREATE INDEX IX_USER_RISK_DETAIL_USER ON USER_RISK_DETAIL(user_id);

ALTER TABLE USER_RISK_DETAIL
  ADD CONSTRAINT FK_USER_RISK_DETAIL_USER
  FOREIGN KEY (user_id) REFERENCES USER_PROFILE(user_id);


